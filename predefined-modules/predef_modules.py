# Using Python's In-build modules.

import math
import operator

a = 9
b = 1

print(f"Calculate 10 ^ 2 using in-build module math:  {math.pow(10, 2)}")

print(f"Addition using in-build module operator : {operator.add(a, b)}")

print(f"Subtraction using in-build module operator : {operator.sub(a, b)}")

print(f"Multiplication using in-build module operator : {operator.mul(a, b)}")

print(f"Division using in-build module operator : {operator.truediv(a, b)}")
